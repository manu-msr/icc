/**
 * Enumeración para representar a un planeta.
 */
public enum Planeta {

   /** El planeta mercurio. */
   MERCURIO,
   /** El planeta venus. */
   VENUS,
   /** El planeta tierra. */
   TIERRA,
   /** El planeta marte. */
   MARTE,
   /** El planeta jupiter. */
   JUPITER,
   /** El planeta saturno. */
   SATURNO,
   /** El planeta urano. */
   URANO,
   /** El planeta neptuno. */
   NEPTUNO,
   /** El planeta plutón. */
   PLUTON;
}
